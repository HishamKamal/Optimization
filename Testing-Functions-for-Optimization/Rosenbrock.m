function [f, g] = Rosenbrock(x)

N = length(x);

f = sum(100 * (x(2 : N) - x(1 : N - 1).^2).^2 + (x(1 : N - 1) - 1).^2);

g = zeros(1, N);
if (N > 2) 
    g(2 : N - 1) = -400 * x(2 : N - 1) .* (x(3 : N) - x(2 : N - 1) .* x(2 : N - 1)) + 2 * (x(2 : N - 1) - 1) + 200 * (x(2 : N - 1) - x(1 : N - 2) .* x(1 : N - 2));
    g(1) = -400 * (x(2) - x(1) * x(1)) * x(1) + 2 * (x(1) - 1);
    g(N) = 200 * (x(N) - x(N - 1)^2);
else
    g(1) = -400 * (x(2) - x(1) * x(1)) * x(1) + 2 * (x(1) - 1);
    g(2) = 200 * (x(2) - x(2 - 1)^2);
end    
