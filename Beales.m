function [f, g] = Beales(in)

x = in(1);
y = in(2);

f = (1.5 - x + x * y)^2 + (2.25 - x + x * y^2)^2 + (2.625 - x + x * y^3)^2;

g(1) = 2 * (1.5 - x + x * y) * (-1 + y) + ...
       2 * (2.25 - x + x * y^2) * (-1 + y^2) + ...
       2 * (2.625 - x + x * y^3) * (-1 + y^3);

g(2) = 2 * (1.5 - x + x * y) * x + ...
       2 * (2.25 - x + x * y^2) * 2 * x * y + ...
       2 * (2.625 - x + x * y^3) * 3 * x * y^2;
